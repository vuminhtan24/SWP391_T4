package model;

import java.sql.Timestamp;

/**
 *
 * @author k16
 */
public class Blog {

    private int blogId;
    private String title;
    private String context;
    private Timestamp created_at;
    private Timestamp updated_at;
    private String img_url;
    private String pre_context;
    private String status;
    private User owner;
    private Category category;

    public Blog() {
    }

    public int getBlogId() {
        return blogId;
    }

    public void setBlogId(int blogId) {
        this.blogId = blogId;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getContext() {
        return context;
    }

    public void setContext(String context) {
        this.context = context;
    }

    public Timestamp getCreated_at() {
        return created_at;
    }

    public void setCreated_at(Timestamp created_at) {
        this.created_at = created_at;
    }

    public Timestamp getUpdated_at() {
        return updated_at;
    }

    public void setUpdated_at(Timestamp updated_at) {
        this.updated_at = updated_at;
    }

    public String getImg_url() {
        return img_url;
    }

    public void setImg_url(String img_url) {
        this.img_url = img_url;
    }

    public String getPre_context() {
        return pre_context;
    }

    public void setPre_context(String pre_context) {
        this.pre_context = pre_context;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public User getOwner() {
        return owner;
    }

    public void setOwner(User owner) {
        this.owner = owner;
    }

    public Category getCategory() {
        return category;
    }

    public void setCategory(Category category) {
        this.category = category;
    }

    @Override
    public String toString() {
        return "Blog{" + "blogId=" + blogId + ", title=" + title + ", context=" + context + ", created_at=" + created_at + ", updated_at=" + updated_at + ", img_url=" + img_url + ", pre_context=" + pre_context + ", status=" + status + ", owner=" + owner + ", category=" + category + '}';
    }

}
